namespace PolygonApp.Models
{
    /// <summary>
    /// Представляет координаты точки в 2D-пространстве.
    /// </summary>
    public class Point
    {
        /// <summary>
        /// Координата X точки.
        /// </summary>
        public double X { get; set; }

        /// <summary>
        /// Координата Y точки.
        /// </summary>
        public double Y { get; set; }
    }
}
